// Addition
let sum = 10 + 5;
console.log("Sum:", sum);

// Subtraction
let difference = 10 - 5;
console.log("Difference:", difference);

// Multiplication
let product = 10 * 5;
console.log("Product:", product);

// Division
let quotient = 10 / 5;
console.log("Quotient:", quotient);

// Modulus (Remainder)
let remainder = 10 % 5;
console.log("Remainder:", remainder);

// Exponentiation
let exponent = 10 ** 5;
console.log("Exponent:", exponent);